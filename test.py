from pygcode.generator import GCodeGen
g = GCodeGen()
g.set(safe_z=5, x0=0, y0=0, z0=0)
g.set(free_speed=1000, mill_speed=200, spindle_speed=5000)
g.set(tool_d=2)
g.set(max_depth_per_pass=0.2)
g.start_step("milling.gcode")
g.move_away(10,10)
g.spiral_hole(d=3, x=10, y=10, z=0, depth=5)
g.move_away(-10,10)
g.rect_vol(xc=-10, yc=10, zc=-10, xdim=10, ydim=15, depth=1, overlapping=0.4)
g.move_away(-10,-10)
g.plane(-10,-10,-10,5,5)
g.move_away(100,100)
g.end_step()
